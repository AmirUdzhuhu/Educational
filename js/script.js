// Арифметические операторы
// можно складывать вместе любое количество чисел или переменных
var addition = 1 + 1; // сложение
var subtraction = 2 - 1; // вычитание
var multiplication = 2 * 2; // умножение
var division = 6 / 3; // деление
var modulo_division = 38 % 5; // деление по модулю (остаток от деления)

var a = 0, b = 10; // постинкремент
